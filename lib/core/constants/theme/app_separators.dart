import "package:flutter/material.dart";

/// The constant separators used in the app
class AppSeparators {
  /// Vertical small separator
  ///
  /// ```dart
  /// SizedBox(height: 10)
  /// ```
  static const Widget vSmall = SizedBox(
    height: 10,
  );

  /// Vertical medium separator
  ///
  /// ```dart
  /// SizedBox(height: 20)
  /// ```
  static const Widget vMedium = SizedBox(
    height: 20,
  );

  /// Vertical large separator
  ///
  /// ```dart
  /// SizedBox(height: 40)
  /// ```
  static const Widget vLarge = SizedBox(
    height: 40,
  );

  /// Horizontal small separator
  ///
  /// ```dart
  /// SizedBox(width: 10)
  /// ```
  static const Widget hSmall = SizedBox(
    width: 10,
  );

  /// Horizontal medium separator
  ///
  /// ```dart
  /// SizedBox(width: 20)
  /// ```
  static const Widget hmedium = SizedBox(
    width: 20,
  );

  /// Horizontal large separator
  ///
  /// ```dart
  /// SizedBox(width: 40)
  /// ```
  static const Widget hLarge = SizedBox(
    width: 40,
  );
}
